# 13. Write a short program that prints the numbers 1 to 10 using a for loop. Then write an equivalent program that prints the numbers 1 to 10 using a while loop.

print('For Loop:')
for i in range(1, 11):
    print(i)

print('-----------')

print('While Loop:')
j = 1
while j < 11:
    print(j)
    j = j + 1